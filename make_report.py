#!/usr/bin/env python3
"""
make_report.py
Generate a tabbed HTML report and a multi-page PDF from monitor output PNGs.

HTML: three tabs, linked PNGs by path (open in browser on cluster or copy
      plots dir to local machine).
PDF:  all plots as sequential pages, one per PNG.

Usage:
    python make_report.py --model TOM12_TJ_HA00
    python make_report.py --model TOM12_TJ_HA00 --year-start 1995 --year-end 2000
"""

import argparse
import glob
import re
import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
OUT_DIR_DEFAULT = Path(
    "/gpfs/home/mep22dku/scratch/NATL_MONITOR/plots"
)


def find_plot(plots_dir: Path, pattern: str) -> Path | None:
    matches = sorted(plots_dir.glob(pattern), key=lambda f: f.stat().st_mtime,
                     reverse=True)
    return matches[0] if matches else None


def collect_plots(model: str, plots_dir: Path,
                  year_start: int, year_end: int) -> dict:
    """Find all relevant PNGs in plots_dir (the report subfolder)."""
    def g(pattern):
        return find_plot(plots_dir, pattern)

    return {
        # Physical — maps use year_start/year_end; ts may differ (use glob)
        "phys_monitor":  g(f"{model}_na_monitor_*.png"),
        "phys_ts":       g(f"{model}_ts_physical_*.png"),
        # BGC
        "bgc_monitor":   g(f"{model}_bgc_monitor_annual_*.png"),
        "bgc_ts":        g(f"{model}_ts_bgc_*.png"),
        # Ecosystem
        "eco_monitor":   g(f"{model}_eco_monitor_*.png"),
    }


