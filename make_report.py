def img_card(title: str, fpath, plots_dir: Path) -> str:
    if fpath is None or not Path(fpath).exists():
        return f'<div class="plot-card"><h2>{title}</h2><div class="missing">Plot not found</div></div>'
    # Use filename only — HTML lives in the same directory as the plots
    rel = Path(fpath).name
    return (f'<div class="plot-card"><h2>{title}</h2>'
            f'<img src="./{rel}" alt="{title}"></div>')


def make_html(model: str, year_start: int, year_end: int,
              plots: dict, out_path: Path, plots_dir: Path):
    html = HTML_TEMPLATE.format(
        title=f"NA Monitor — {model} — {year_start}–{year_end}",
        subtitle=f"North Atlantic diagnostic monitor suite  ·  years {year_start}–{year_end}",
        model=model,
        year_start=year_start,
        year_end=year_end,
        phys_monitor_card=img_card("Physical monitor (maps)",   plots["phys_monitor"], plots_dir),
        phys_ts_card=     img_card("Physical timeseries",       plots["phys_ts"],      plots_dir),
        bgc_monitor_card= img_card("BGC monitor (maps)",        plots["bgc_monitor"],  plots_dir),
        eco_monitor_card= img_card("Ecosystem monitor (maps)",  plots["eco_monitor"],  plots_dir),
        bgc_ts_card=      img_card("BGC & plankton timeseries", plots["bgc_ts"],       plots_dir),
    )
    out_path.write_text(html)
    print(f"  HTML written: {out_path}")


