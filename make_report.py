def make_pdf(model: str, year_start: int, year_end: int,
             plots: dict, out_path: Path):
    import matplotlib.pyplot as plt
    from matplotlib.backends.backend_pdf import PdfPages
    from matplotlib.image import imread

    # Ordered pages with titles
    pages = [
        ("Physical monitor (maps)",   plots["phys_monitor"]),
        ("Physical timeseries",       plots["phys_ts"]),
        ("BGC monitor (maps)",        plots["bgc_monitor"]),
        ("BGC & plankton timeseries", plots["bgc_ts"]),
        ("Ecosystem monitor (maps)",  plots["eco_monitor"]),
    ]

    with PdfPages(out_path) as pdf:
        # Cover page
        fig = plt.figure(figsize=(11.7, 8.3))   # A4 landscape
        fig.patch.set_facecolor("#1a1a1a")
        fig.text(0.5, 0.55, f"NA Monitor Suite",
                 ha="center", va="center", fontsize=28,
                 color="#f5f5f5", fontweight="light")
        fig.text(0.5, 0.45, f"{model}  ·  {year_start}–{year_end}",
                 ha="center", va="center", fontsize=18, color="#aaaaaa")
        fig.text(0.5, 0.35, "North Atlantic diagnostic monitor",
                 ha="center", va="center", fontsize=12, color="#666666")
        pdf.savefig(fig, facecolor=fig.get_facecolor())
        plt.close(fig)

        for title, fpath in pages:
            if fpath is None or not Path(fpath).exists():
                print(f"  WARNING: {title} — file not found, skipping page.")
                continue
            img = imread(str(fpath))
            # Scale to A4 landscape
            h, w = img.shape[:2]
            fig_w, fig_h = 11.7, 8.3
            aspect = w / h
            if aspect > fig_w / fig_h:
                img_w, img_h = fig_w - 0.4, (fig_w - 0.4) / aspect
            else:
                img_h, img_w = fig_h - 0.8, (fig_h - 0.8) * aspect

            fig = plt.figure(figsize=(fig_w, fig_h))
            fig.patch.set_facecolor("white")
            # Title strip
            fig.text(0.5, 0.97, title, ha="center", va="top",
                     fontsize=10, color="#444444")
            fig.text(0.5, 0.93, f"{model}  ·  {year_start}–{year_end}",
                     ha="center", va="top", fontsize=8, color="#aaaaaa")
            ax = fig.add_axes([
                (fig_w - img_w) / 2 / fig_w,
                (fig_h - img_h) / 2 / fig_h - 0.03,
                img_w / fig_w,
                img_h / fig_h,
            ])
            ax.imshow(img)
            ax.axis("off")
            pdf.savefig(fig, facecolor="white")
            plt.close(fig)
            print(f"  PDF page: {title}")

    print(f"  PDF written: {out_path}")


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--model",      required=True)
    parser.add_argument("--year-start", type=int, default=None)
    parser.add_argument("--year-end",   type=int, default=None)
    parser.add_argument("--plots-dir",  type=Path, default=OUT_DIR_DEFAULT)
    parser.add_argument("--out-dir",    type=Path, default=None,
                        help="Output dir for HTML/PDF (default: same as plots-dir)")
    parser.add_argument("--skip-html",  action="store_true")
    parser.add_argument("--skip-pdf",   action="store_true")
    args = parser.parse_args()

    out_dir = args.out_dir or args.plots_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    # Auto-detect year range from available timeseries PNGs if not given
    if args.year_start is None or args.year_end is None:
        ts_files = sorted(args.plots_dir.glob(f"{args.model}_ts_physical_*.png"))
        if ts_files:
            m = re.search(r"_(\d{4})_(\d{4})\.png$", ts_files[-1].name)
            if m:
                args.year_start = args.year_start or int(m.group(1))
                args.year_end   = args.year_end   or int(m.group(2))
    if args.year_start is None or args.year_end is None:
        print("ERROR: could not auto-detect year range. Pass --year-start and --year-end.")
        sys.exit(1)

    print(f"\nCollecting plots for {args.model} {args.year_start}–{args.year_end}…")
    print(f"  Searching in: {args.plots_dir}")
    print(f"  PNGs found: {[f.name for f in sorted(args.plots_dir.glob('*.png'))]}")
    plots = collect_plots(args.model, args.plots_dir, args.year_start, args.year_end)
    for k, v in plots.items():
        print(f"  {k}: {v.name if v else 'NOT FOUND'}")

    yr = f"{args.year_start}_{args.year_end}"

    if not args.skip_html:
        make_html(args.model, args.year_start, args.year_end, plots,
                  out_path=out_dir / f"{args.model}_monitor_{yr}.html",
                  plots_dir=args.plots_dir)

    if not args.skip_pdf:
        make_pdf(args.model, args.year_start, args.year_end, plots,
                 out_path=out_dir / f"{args.model}_monitor_{yr}.pdf")

    print("\nDone.")


if __name__ == "__main__":
    main()
